#!/usr/bin/env node
/**
 * Writes packages/build-info/src/generated.ts from root package.json + git + clock.
 * Invoked automatically by Vite config and API build/dev scripts.
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

import { resolveBuildMeta } from './resolve-build-meta.mjs';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const outFile = join(root, 'packages/build-info/src/generated.ts');

const meta = resolveBuildMeta({
  environment: process.env.APP_ENV || process.env.VITE_APP_ENV || process.env.NODE_ENV,
});

mkdirSync(dirname(outFile), { recursive: true });

const source = `/* AUTO-GENERATED by scripts/generate-build-meta.mjs — do not edit by hand */
import type { BuildInfo } from './types.js';

export const GENERATED_BUILD_META = ${JSON.stringify(meta, null, 2)} as const satisfies BuildInfo;
`;

writeFileSync(outFile, source, 'utf8');
console.log(
  `[build-meta] v${meta.version} build=${meta.build} env=${meta.environment} → packages/build-info/src/generated.ts`,
);
